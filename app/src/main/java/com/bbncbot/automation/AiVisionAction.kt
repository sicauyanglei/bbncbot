package com.bbncbot.automation

/**
 * AI 视觉决策预定义动作
 *
 * 用户需求：有些不能处理的问题可以截图交给 API 来处理。
 *
 * 状态机进入 UNKNOWN 场景时，截图交给智谱 GLM-4.6V-Flash 视觉模型，
 * AI 只能从这 5 个动作中返回一个，由 AutomationController 执行。
 *
 * 设计原则：
 * - 动作集合最小化（5 个），避免 AI 返回无法解析的复杂指令
 * - 每个动作都有明确的执行逻辑（点关闭按钮/点领取按钮/按返回/跳过任务/等待）
 * - 安全性：所有动作都不会造成不可逆操作（不会自动输入、不会自动购买）
 */
enum class AiVisionAction {
    /** 点击关闭按钮（×/关闭/知道了/确定）关闭弹窗 */
    CLICK_CLOSE,

    /** 点击领取/确认按钮（领取奖励/领取肥料/确定）领取奖励 */
    CLICK_CLAIM,

    /** 按返回键（无法处理或应该退出当前页） */
    PRESS_BACK,

    /** 跳过当前任务（页面无肥料价值，如分享/评价/会员推销） */
    SKIP_TASK,

    /** 等待（页面正在加载或倒计时中，不应操作） */
    WAIT
}
